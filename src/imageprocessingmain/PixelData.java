//*********************** supporting class **********************************//

public class PixelData
{
	public int a,x,y,r,g,b,count;
	public PixelData(int x,int y,int a,int r,int g,int b,int count)
	{
		this.a=a;
		this.x=x;
		this.y=y;
		this.r=r;
		this.g=g;
		this.b=b;
		this.count=count;
		
	}
}
	
	